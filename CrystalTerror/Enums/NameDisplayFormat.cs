namespace CrystalTerror.Enums;

/// <summary>
/// Defines how character names are displayed in the UI.
/// </summary>
public enum NameDisplayFormat
{
    /// <summary>
    /// Show full name (e.g., "John Doe").
    /// </summary>
    FullName,

    /// <summary>
    /// Show first name only (e.g., "John").
    /// </summary>
    FirstName,

    /// <summary>
    /// Show last name only (e.g., "Doe").
    /// </summary>
    LastName,

    /// <summary>
    /// Show initials (e.g., "J.D.").
    /// </summary>
    Initials
}
